// basic example to give an understanding of need and work of loops

public class loop_vision {
    public static void main(String[] args){

        int i = 0;
    
        while(i < 5){
            System.out.println("While loop in JAVA");
            i++;
        }
       
        int j =0;
        
        do{
            System.out.println("Do-while loop");
            j++;
        } while (j < 5);
    }
}
