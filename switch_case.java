// Understanding switch case using a simple example 

public class switch_case {

    public static void main(String[] args){
        int num = 220;
        switch(num){
            case 200: System.out.println("Case1 executed");
                break;
            case 300: System.out.println("Case2 executed");
                break;
            case 100: System.out.println("Case3 executed");
                break;
            default: System.out.println("None of the above cases executed");
        }
    }
}
    

